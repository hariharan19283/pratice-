class Datatypes1
{
public static void main(String[] args)
{
byte a=114;
System.out.println("Value of byte type variable is: " + a);
short b=9000;
System.out.println("Value of short type variable is: " + b);
int c=5;
System.out.println("Value of int type variable is: " + c);
long l=90000000000L;
System.out.println("Value of long type variable is: " + l);
}
}